number = input("Enter a nuumber, and I'll tell you if it's even or odd: ")
number = int(number)

if number % 2 == 0 :
    print("\nThe number " + str(number) + " is even.")
else:
    print("\nThe number " + str(number) + " is odd.")
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    